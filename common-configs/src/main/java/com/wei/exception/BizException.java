package com.wei.exception;

import org.springframework.http.HttpStatus;
import org.springframework.util.StringUtils;

/**
 * @Describe
 * @Author wei.peng
 * @Date 2021年04月14日
 */
public class BizException extends RuntimeException {

    private static final long serialVersionUID = 1L;

    /**
     * 错误码
     */
    protected String errorCode;
    /**
     * 错误信息
     */
    protected String errorMsg;

    public BizException() {
        super();
    }

    public BizException(String errorMsg) {
        super(errorMsg);
        this.errorMsg = errorMsg;
    }

    public BizException(String errorCode, String errorMsg) {
        super(errorCode);
        this.errorCode = errorCode;
        this.errorMsg = errorMsg;
    }

    public BizException(HttpStatus status, String message) {
        super(String.valueOf(status == null ? message : StringUtils.isEmpty(message) ? status.value() : message));
       if(status == null){
           return;
       }
        this.errorCode = String.valueOf(status.value());
        this.errorMsg = StringUtils.isEmpty(message) ? status.getReasonPhrase() : message;
    }

    public BizException(String errorCode, String errorMsg, Throwable cause) {
        super(errorCode, cause);
        this.errorCode = errorCode;
        this.errorMsg = errorMsg;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorMsg() {
        return errorMsg;
    }

    public void setErrorMsg(String errorMsg) {
        this.errorMsg = errorMsg;
    }

    @Override
    public String getMessage() {
        return errorMsg;
    }

    @Override
    public Throwable fillInStackTrace() {
        return this;
    }

}